import streamlit as st

# 0_Home.py - 開発背景と課題定義
#
# 【設計意図】
#  ポートフォリオのトップ画面として「開発背景」「プロジェクト概要」「解決すべき課題」などを表示する目的とする

def main():
    st.title("💼 勤怠管理AIチャットボットのポートフォリオ")

    # 開発の動機
    st.header("📌 開発の背景と動機")
    st.markdown("""
    ### **「実務の痛み」をITで解決する。**

    私は現在、勤怠管理ツールの担当として、新規入職者からの問い合わせ対応や、ルールの確認・エスカレーション業務に従事しています。

    日々の業務の中で直面した以下の課題を解決するため、本アプリの開発を決意しました。

    * **課題**: 規定が複雑で、担当者や上司への確認作業（エスカレーション）で業務負荷が発生している。
    * **狙い**: AI（RAG）を活用し、誰もが即座に正確な回答を得られる仕組みを構築することで、付帯作業時間を削減し、組織全体の生産性を向上させる。
    """)

    st.divider()

    # プロジェクトの概要
    col1, col2 = st.columns(2)
    with col1:
        st.subheader("💡 プロジェクトについて")
        st.markdown("""
        - 就業規則や勤怠ルールに関する質問に、AIが社内の勤怠ルールPDFに基づいて回答するRAG（検索拡張生成）システムです。
        - ユーザーから解決できなかった内容を管理者に問い合わせして勤怠ルールPDFを更新し改善するPDCAサイクルの設計をしています
        """)

    with col2:
        st.subheader("🌟 解決したい課題")
        st.markdown("""
        - Q&Aチャットボット導入による従業員の自己解決率の向上
        - 管理部門への「同じ質問」を自動化し、対応工数を低減
        - PDF更新だけでチャットボットに反映されるので更新作業負担低減
        """)

    st.divider()

    st.info("👈 左側のサイドメニューから、各機能や詳しいシステム構成をご覧いただけます。")

if __name__ == "__main__":
    main()